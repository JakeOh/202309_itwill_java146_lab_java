package com.itwill.condition01;

public class ConditionMain01 {

    public static void main(String[] args) {
        // 조건문(conditional statement)
        
        int number = -123;
        if (number > 0) {
            // 조건식을 만족하는 경우에 실행할 코드 블록
            System.out.println("양수");
        }

        System.out.println("=== 프로그램 끝 ===");
    }

}
