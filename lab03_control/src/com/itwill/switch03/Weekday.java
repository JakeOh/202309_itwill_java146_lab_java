package com.itwill.switch03;

// enum: enumerate type(열거형 타입). 상수(constant)들을 정의하는 특별한 종류의 class.

// 변수(variable): 값(데이터)을 저장하기 위한 메모리 공간.
// 상수(constant): 초기화한 이후에는 값을 변경할 수 없는 변수.
// 리터럴(literal): 고정된 값. (예) 1, 2, 3.14, "abcd", ...
// int x = 1; -> x: 변수, 1: 리터럴
// final int x = 1; -> x: 상수 변수(constant variable).

public enum Weekday {
    SUN, MON, TUE, WED, THU, FRI, SAT;
}
